# Tuples dan Set

# List
# Menggunakan kurung siku dan fleksible
data_list = [10,2,4,3,2]
print(f"data_list = {data_list}\n")

# Tuples
# Menggunakan kurung
# Permanen, datanya tidak dapat dirubah-rubah
data_tuples = (10,2,4,3,2)
print(f"data_tuples = {data_tuples}")
print(f"data_tuples[0] = {data_tuples[0]}\n")

# Tuples tidak dapat dilakukan
# data_tuples[1] = 3
# data_tuples.append(8)

# Sets {himpunan}
data_sets = {10,4,3,2,4,7,6,5}
print(f"data_sets = {data_sets}")

# Sets bisa di-add
